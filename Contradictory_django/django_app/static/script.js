// Function to change the language based on the selected option
function changeLanguage() {
    var selectedLanguage = document.getElementById('language-selector').value;
    document.getElementById('selected-language').value = selectedLanguage;
    const url = window.location.href.split('?')[0];  // Remove query parameters from URL
    window.location.href = `${url}?lang=${selectedLanguage}`;
}

function updatePageLanguage(translation) {
    console.log('Updating page language:', translation);
    document.getElementById('page-title').innerText = translation['page_title'];
    document.getElementById('language').innerText = translation['language_selector'];
    document.getElementById('premises_1').innerText = translation['premises'];
    document.getElementById('hypotheses_1').innerText = translation['hypotheses'];
    document.getElementById('submit-button').innerText = translation['submit'];
    document.getElementById('result-title').innerText = translation['result_title'];
    document.getElementById('similarity-label').innerText = translation['similarity_label'];
    document.getElementById('go-back-link').innerText = translation['go_back_link'];
  }

// Function to set the language based on the query parameter
function setLanguage() {
    const urlParams = new URLSearchParams(window.location.search);
    const selectedLanguage = urlParams.get('lang');
    if (selectedLanguage) {
        document.getElementById('language-selector').value = selectedLanguage;

        // logic to change the page language here
        if (selectedLanguage) {
            // Change the page language to English
            updatePageLanguage(translations[selectedLanguage])
        }
    }
}

// Call the setLanguage function when the page loads
window.onload = setLanguage;